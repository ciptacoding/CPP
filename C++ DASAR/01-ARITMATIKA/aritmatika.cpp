#include <iostream>
using namespace std;

int main (){
	int a;
	float b;
	float hasil;
	cout << "===== Penjumlahan ====\n";
	cout << "masukkan nilai : ";
	cin >> a;
	cout << "masukkan nilai berikutnya : ";
	cin >> b;
	hasil = a + b;
	cout << "hasil dari " << a << " + " << b << " = " << hasil << endl;
	cout << "                     \n";
	cout << "                     \n";
	
	
	cout << "===== Pengurangan ====\n";
	cout << "masukkan nilai : ";
	cin >> a;
	cout << "masukkan nilai berikutnya : ";
	cin >> b;
	hasil = a - b; 
	cout << "hasil dari " << a << " - " << b << " = " << hasil << endl;
	cout << "                     \n";
	cout << "                     \n";
	
	cout << "===== Perkalian ====\n";
	cout << "masukkan nilai : ";
	cin >> a;
	cout << "masukkan nilai berikutnya : ";
	cin >> b;
	hasil = a * b; 
	cout << "hasil dari " << a << " x " << b << " = " << hasil << endl;
	cout << "                     \n";
	cout << "                     \n";
	
	cout << "===== Pembagian ====\n";
	cout << "masukkan nilai : ";
	cin >> a;
	cout << "masukkan nilai berikutnya : ";
	cin >> b;
	hasil = a / b; 
	cout << "hasil dari " << a << " / " << b << " = " << hasil << endl;
	cout << "                     \n";
	cout << "                     \n";
	
	return 0;
}
